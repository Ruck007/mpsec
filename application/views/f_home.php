<p>เราต้องออกกำลัง ต้องฝึกร่างกายเพื่อให้เรามีภูมิคุ้มกันโรคภัยต่างๆ หากเราไม่ได้วิ่งทุกวัน ไม่เคยซ้อมวิ่งเลย 
แล้วจู่ๆให้ลุกไปวิ่งมาราธอนเลยคงจะเป็นไปไม่ได้ ทั้งใจและกายคงจะเกิดการต่อด้านอย่างหนัก การลงทุนก็เช่นกัน ไม่สามารถที่จะเร่งให้เกิดดอกออกผลอย่างรวดเร็วให้ทันใจได้ หากปราศจากการเตรียมความพร้อมทั้งด้านความรู้ ความเข้าใจและการบริหารความเสี่ยง ซึ่งส่วนใหญ่มักสั่งสมจากประสบการณ์การลงทุน (เจ็บแล้วเจ็บอีก)</p>
