<p>จากประสบการณ์การทำงานในฐานะ Fund Manager มาครึ่งทศวรรษ พบกับนักลงทุนมากมายหลายประเภททั้งพอร์ทเล็กยันพอร์ทใหญ่ เพื่อนฝูงที่หลังจากที่ได้ทราบว่าเราย้ายจากวงการสถาปนิกมาสายการเงิน ก็แวะเวียนเข้ามาปรึกษาเรื่องการลงทุนก็มีไม่น้อย ยิ่งล่าสุดได้มีโอกาสนำเสนอผลงานต่อหน้าคนจำนวนมาก ทั้งเคยลงทุนและไม่เคยลงทุน มีคำถามหลายคำถามที่สะท้อนความเชื่อที่บางทีก็แปลกอยู่พอสมควร จึงจะขอใช้พื้นที่บนเพจ the nightwatch นี้ ในการค่อยเรียบเรียง เล่าสู่กันฟัง รวมถึงเผยวิธีการรับมือไว้ด้วย เผื่อว่า จะตรงใจและนำไปปรับใช้กันได้ครับ</p>
