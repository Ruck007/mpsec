<ul class="cp-post-tools">
<li><i class="icon-1"></i> May 10, 2016</li>
<li><i class="icon-2"></i> Roy Miller</li>
<li><i class="icon-3"></i> Lifestyle</li>
<li><i class="icon-4"></i> 57 Comments</li>
</ul>
<p>กองทุนส่วนบุคคล หรือ Private Fund (PF)นั้น ในปัจจุบันยังคงเป็นบริการระดับพรีเมี่ยม สำหรับนักลงทุนหรือลูกค้าธนาคารที่มีเงินฝากหรือบัญชีเงินที่นำไปลงทุนได้สูงกว่า 40-50 ล้านบาทขึ้นไป โดยจุดขายคือ บริการจัดการเงินลงทุนให้โดยทีมผู้จัดการกองทุน ผู้มีประสบการณ์และความชำนาญ บนนโยบายที่กำหนดได้ตามความต้องการของนักลงทุนแต่ละรายเลยทีเดียว เรียกได้ว่า บริหารกันแบบตัวต่อตัว เหมือนมีกุนซือจัดทัพลงทุนให้ถึงบ้าน แต่ก่อนที่จะลงรายละเอียดมากขึ้น เรามาดูกันก่อนว่า Private Fund อยู่ตรงไหนของอุตสาหกรรมจัดการกองทุน</p>
