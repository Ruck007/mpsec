<p>ขึ้นชื่อว่า ผู้จัดการกองทุนแล้ว เป็นอาชีพที่หลายต่อหลายคน (อย่างน้อยก็เพื่อนแม่ของผม) ยังไม่ค่อยคุ้นว่า ทำหน้าที่อะไร มีความสำคัญยังไง หลายครั้งก็เข้าใจว่าเป็นมาเกตติ้งเชียร์หุ้นอยู่บ่อยครั้ง แต่ความจริงแล้วในฐานะ Fund Manager ทำหน้าที่อะไร และมีความยากง่าย ท้าทายอย่างไร ขอเล่าให้ฟังง่ายๆ นะครับ “ผมบริหารจัดการเงินลงทุนให้เกิดดอกผลสูงสุด ภายใต้ความเสี่ยงที่เหมาะสมกับนักลงทุน” </p>
